class FCDMetric(MolecularMetric):
    """Frechet ChemNet Distance via :mod:`fcd_torch`."""

    name = "fcd"

    def __init__(self, device: str = "cpu", n_jobs: int = 1) -> None:
        self.device = device
        self.n_jobs = n_jobs
        # ``fcd_torch.FCD`` is the actual type, but the package ships no
        # type stubs so we type the slot as ``Any`` to keep pyright quiet.
        self._fcd: Any = None

    def _ensure(self) -> Any:
        if self._fcd is None:
            from fcd_torch import FCD

            self._fcd = FCD(device=self.device, n_jobs=self.n_jobs)
        return self._fcd

    def compute(
        self,
        generated: Sequence[str],
        reference: Sequence[str] | None = None,
    ) -> float:
        if reference is None:
            raise ValueError("FCDMetric requires reference SMILES.")
        gen_valid = _drop_invalid(generated)
        if not gen_valid:
            return float("inf")
        fcd = self._ensure()
        # fcd_torch's FCD object is callable: __call__(gen, ref) → float.
        return float(fcd(list(gen_valid), list(reference)))


class SNNMetric(MolecularMetric):
    """Average Tanimoto similarity to the nearest reference neighbour.

    Mirrors ``moses.metrics.SNNMetric`` with Morgan fingerprints
    (radius 2, 1024 bits) and ``max``-aggregation across reference.
    """

    name = "snn"

    def compute(
        self,
        generated: Sequence[str],
        reference: Sequence[str] | None = None,
    ) -> float:
        if reference is None:
            raise ValueError("SNNMetric requires reference SMILES.")
        gen_valid = _drop_invalid(generated)
        ref_valid = _drop_invalid(reference)
        if not gen_valid or not ref_valid:
            return 0.0
        gen_fps = _morgan_fingerprints(gen_valid)
        ref_fps = _morgan_fingerprints(ref_valid)
        # (n_gen, n_ref): for each gen, max similarity over ref.
        sim = _tanimoto_matrix(gen_fps, ref_fps)
        return float(sim.max(axis=1).mean())


class IntDivMetric(MolecularMetric):
    """Internal diversity ``1 - mean Tanimoto`` over the generated set."""

    name = "int_div"

    def compute(
        self,
        generated: Sequence[str],
        reference: Sequence[str] | None = None,
    ) -> float:
        del reference
        gen_valid = _drop_invalid(generated)
        if not gen_valid:
            return 0.0
        fps = _morgan_fingerprints(gen_valid)
        sim = _tanimoto_matrix(fps, fps)
        return float(1.0 - sim.mean())


class FiltersMetric(MolecularMetric):
    """Fraction of generated molecules passing the MOSES filter set."""

    name = "filters"

    def compute(
        self,
        generated: Sequence[str],
        reference: Sequence[str] | None = None,
    ) -> float:
        del reference
        if not generated:
            return 0.0
        passes = [_passes_moses_filters(s) for s in generated]
        return float(np.mean(passes))


class ScaffoldSplitMetric(MolecularMetric):
    """Fraction of generated mols whose Bemis-Murcko scaffolds are
    novel relative to the reference set's scaffolds."""

    name = "scaffold_novelty"

    def compute(
        self,
        generated: Sequence[str],
        reference: Sequence[str] | None = None,
    ) -> float:
        if reference is None:
            raise ValueError("ScaffoldSplitMetric requires reference SMILES.")
        gen_valid = _drop_invalid(generated)
        if not gen_valid:
            return 0.0
        ref_scaffolds = set(_scaffold_counter(reference).keys())
        gen_scaffolds = _scaffold_counter(gen_valid)
        if not gen_scaffolds:
            return 0.0
        novel = [s for s in gen_scaffolds if s not in ref_scaffolds]
        return len(novel) / len(gen_scaffolds)


