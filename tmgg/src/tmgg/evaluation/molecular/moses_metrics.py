"""MOSES metrics (Table 5 in DiGress): FCD/SNN/IntDiv/Filters/ScaffoldSplit.

Reimplemented locally because the upstream ``moses.metrics`` module is
not importable on this stack. ``moses/metrics/SA_Score/sascorer.py`` does
``from rdkit.six import iteritems`` (gone since 2019), the metrics
helpers call ``DataFrame.append(...)`` (removed in pandas 2.0) and rely
on ``np.NaN`` (removed in numpy 2.0), and the legacy
``GetMorganFingerprintAsBitVect`` it uses segfaults on modern RDKit for
some SMILES. We therefore reimplement the four metrics this project
consumes (``SNN``, ``IntDiv``, ``Filters``, ``ScaffoldNovelty``) directly
against RDKit's modern API. The MCF + WEHI PAINS SMARTS catalogues that
``Filters`` needs are vendored at module bottom; both lists are the
canonical MOSES tables shipped with ``molsets==0.3.1``. To regenerate
them, run::

    import importlib.util, csv
    from pathlib import Path
    spec = importlib.util.find_spec("moses")
    moses_dir = Path(spec.origin).parent
    with (moses_dir / "metrics" / "mcf.csv").open() as f:
        mcf = [row["smarts"].strip() for row in csv.DictReader(f)]
    with (moses_dir / "metrics" / "wehi_pains.csv").open() as f:
        pains = [row[0].strip() for row in csv.reader(f) if row]

after installing ``molsets`` once in a throw-away environment.

The MOSES filter set keeps molecules with: only allowed atoms
``{C, N, S, O, F, Cl, Br, H}``, no formal charges, no rings of size >= 8,
SMILES that round-trip, and no substructure match against MCF or WEHI
PAINS - identical to ``moses.metrics.utils.mol_passes_filters``.

FCD remains a thin wrapper over ``fcd_torch.FCD``, which works
standalone.
"""

