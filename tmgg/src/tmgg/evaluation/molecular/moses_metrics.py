def _drop_invalid(smiles: Sequence[str]) -> list[str]:
    """Return SMILES that round-trip through RDKit sanitisation."""
    from rdkit import Chem

    out: list[str] = []
    for s in smiles:
        mol = Chem.MolFromSmiles(s)
        if mol is None:
            continue
        try:
            Chem.SanitizeMol(mol)
        except Exception:
            continue
        out.append(s)
    return out


def _morgan_fingerprints(
    smiles: Sequence[str],
    radius: int = 2,
    n_bits: int = 1024,
) -> np.ndarray:
    """Morgan/ECFP fingerprints as a uint8 ``(n, n_bits)`` array.

    Uses the modern ``rdFingerprintGenerator`` API; the old
    ``GetMorganFingerprintAsBitVect`` is deprecated and segfaults on
    modern RDKit for some inputs.
    """
    from rdkit import Chem
    from rdkit.Chem import rdFingerprintGenerator

    gen = rdFingerprintGenerator.GetMorganGenerator(radius=radius, fpSize=n_bits)
    rows: list[np.ndarray] = []
    for s in smiles:
        mol = Chem.MolFromSmiles(s)
        if mol is None:
            rows.append(np.zeros(n_bits, dtype=np.uint8))
            continue
        rows.append(gen.GetFingerprintAsNumPy(mol).astype(np.uint8))
    if not rows:
        return np.zeros((0, n_bits), dtype=np.uint8)
    return np.vstack(rows)


def _tanimoto_matrix(a: np.ndarray, b: np.ndarray) -> np.ndarray:
    """Pairwise Tanimoto similarity between binary fingerprint matrices.

    ``a`` is ``(n, d)``, ``b`` is ``(m, d)``; result is ``(n, m)``.
    """
    a_f = a.astype(np.float64)
    b_f = b.astype(np.float64)
    inter = a_f @ b_f.T
    a_sum = a_f.sum(axis=1, keepdims=True)  # (n, 1)
    b_sum = b_f.sum(axis=1, keepdims=True).T  # (1, m)
    union = a_sum + b_sum - inter
    # If both fingerprints are all zeros their union is 0; mirror MOSES'
    # ``jac[isnan(jac)] = 1`` by treating that as a perfect match.
    with np.errstate(divide="ignore", invalid="ignore"):
        sim = np.where(union > 0, inter / np.maximum(union, 1.0), 1.0)
    return sim


def _murcko_scaffold(smiles: str, *, min_rings: int = 2) -> str | None:
    """Bemis-Murcko scaffold SMILES, or ``None`` if smaller than ``min_rings``.

    Matches ``moses.metrics.utils.compute_scaffold`` semantics: scaffolds
    with fewer than ``min_rings`` rings (default: 2) are dropped.
    """
    from rdkit import Chem
    from rdkit.Chem.Scaffolds import MurckoScaffold

    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return None
    try:
        scaffold = MurckoScaffold.GetScaffoldForMol(mol)
    except (ValueError, RuntimeError):
        return None
    if scaffold is None:
        return None
    n_rings = scaffold.GetRingInfo().NumRings()
    smi = Chem.MolToSmiles(scaffold)
    if not smi or n_rings < min_rings:
        return None
    return smi


def _scaffold_counter(smiles: Sequence[str]) -> dict[str, int]:
    """Count Bemis-Murcko scaffolds across a SMILES list (``None`` dropped)."""
    counts: dict[str, int] = {}
    for s in smiles:
        scaf = _murcko_scaffold(s)
        if scaf is None:
            continue
        counts[scaf] = counts.get(scaf, 0) + 1
    return counts


@lru_cache(maxsize=1)
def _compiled_filters() -> tuple[Any, ...]:
    """Compile the vendored MCF + PAINS SMARTS into RDKit pattern objects."""
    from rdkit import Chem

    patterns: list[Any] = []
    for smi in (*MCF_SMARTS, *PAINS_SMARTS):
        patt = Chem.MolFromSmarts(smi)
        if patt is not None:
            patterns.append(patt)
    return tuple(patterns)


def _passes_moses_filters(smiles: str) -> bool:
    """Mirror ``moses.metrics.utils.mol_passes_filters``.

    Checks: only allowed atoms ``{C,N,S,O,F,Cl,Br,H}``, no formal
    charges, no ring of size >= 8, SMILES round-trips, and no match to
    any MCF or PAINS pattern. Returns ``False`` for invalid SMILES.
    """
    from rdkit import Chem

    allowed = {"C", "N", "S", "O", "F", "Cl", "Br", "H"}
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return False
    ring_info = mol.GetRingInfo()
    if ring_info.NumRings() != 0 and any(len(r) >= 8 for r in ring_info.AtomRings()):
        return False
    if any(atom.GetFormalCharge() != 0 for atom in mol.GetAtoms()):
        return False
    if any(atom.GetSymbol() not in allowed for atom in mol.GetAtoms()):
        return False
    h_mol = Chem.AddHs(mol)
    for patt in _compiled_filters():
        if h_mol.HasSubstructMatch(patt):
            return False
    canon = Chem.MolToSmiles(mol, isomericSmiles=False)
    if not canon:
        return False
    return Chem.MolFromSmiles(canon) is not None


